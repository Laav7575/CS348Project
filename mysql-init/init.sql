CREATE TABLE IF NOT EXISTS Cars (
    cID INT AUTO_INCREMENT NOT NULL,
    make VARCHAR(30) NOT NULL,
    model VARCHAR(30) NOT NULL,
    year INT,
    isElectric BOOLEAN,
    engineSize FLOAT,
    horsePower FLOAT,
    torque FLOAT,
    acceleration FLOAT,
    price FLOAT,
    PRIMARY KEY(cID)
);

-- create tables
CREATE TABLE testCars (
   cID                     INT AUTO_INCREMENT not NULL,
   make                    VARCHAR(50) not NULL,
   model                   VARCHAR(50) not NULL,
   image                   VARBINARY(1000),
   year                    INT,
   isElectric		           BOOLEAN,
   engineSize              float,
   horsePower              float,
   torque                  float,
   acceleration            float,
   price                   float,
   primary key(cID)
);


CREATE TABLE Users (
    uID            INT AUTO_INCREMENT NOT NULL,
    username       VARCHAR(30) UNIQUE,
    email          VARCHAR(100) UNIQUE,
    userPassword   VARCHAR(255),
    isAdmin        BOOLEAN,
    isDeleted      BOOLEAN,
    PRIMARY KEY (uID),
    -- CHECK (username REGEXP '^[A-Za-z0-9_-]+$.'),
    CHECK (regexp_like(`username`, _latin1'^[A-Za-z0-9_.-]+$')), 
    CHECK (email LIKE '%@%')
);

CREATE TABLE Reviews (
  uid              INT,
  cid              INT,
  comment              VARCHAR(255) NOT NULL,
  createdDate DATE NOT NULL,
  updatedDate DATE,
  stars INT,
  PRIMARY KEY (uid, cid),
  FOREIGN KEY (uid) REFERENCES Users(uID),
  FOREIGN KEY (cid) REFERENCES Cars(cID) ON DELETE CASCADE
);

CREATE TABLE Folders (
   fID 			INT AUTO_INCREMENT NOT NULL,
   uID 			INT NOT NULL,
   folderName 		VARCHAR(30) NOT NULL,
   isLikes BOOLEAN DEFAULT FALSE,
   PRIMARY KEY(fID),
   FOREIGN KEY(uID) REFERENCES Users(uID) ON DELETE CASCADE
);

CREATE TABLE Saves(
  fID INT NOT NULL,
  cID INT NOT NULL,
  date DATE NOT NULL,
  FOREIGN KEY (fID) REFERENCES Folders(fID) ON DELETE CASCADE,
  FOREIGN KEY (cID) REFERENCES Cars(cID) ON DELETE CASCADE
);

CREATE TABLE Adds(
  uID INT NOT NULL,
  cID INT NOT NULL,
  date DATE NOT NULL,
  FOREIGN KEY (uID) REFERENCES Users(uID) ON DELETE CASCADE,
  FOREIGN KEY (cID) REFERENCES Cars(cID) ON DELETE CASCADE
);

